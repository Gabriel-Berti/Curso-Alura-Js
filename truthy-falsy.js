"No JavaScript, temos alguns dados que se comportam como se fossem do tipo boolean, ou seja, são interpretados como false ou true"

console.log(1 == true)
console.log(0 == false)
console.log("" == false)

"Se rodarmos esses comandos no terminal, veremos que todas as afirmações que fizemos são verdadeiras, ou seja, tanto o número 0, quanto uma string vazia '' se comportam também como false, e que o número 1 por exemplo, se comporta como true."